class Solution {
public:
    int maxDepth(TreeNode* root) {
        return getMaxDepth(root, 0);
    }

    int getMaxDepth(TreeNode* node, int depth)
    {
        if (node == nullptr) {
            return depth;
        }
        return max(getMaxDepth(node->left, depth + 1), getMaxDepth(node->right, depth + 1));
    }
};
